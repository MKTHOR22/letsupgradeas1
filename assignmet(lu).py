# Taking two inputs from the user
a = float(input("Enter the first number (a): "))
b = float(input("Enter the second number (b): "))

# 1. Floor Division
floor_division_result = a // b
print("Floor Division (a // b):", floor_division_result)

# 2. a power b
power_result = a ** b
print("a to the power of b (a ** b):", power_result)

# 3. Check if two variables have equal value
are_equal = a == b
print("Are a and b equal?", are_equal)